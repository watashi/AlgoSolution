LIMIT = 100000000000
$all = []

def init
  from = 1
  to = 10
  while from < LIMIT
    from.upto(to - 1) do |i|
      s = i.to_s
      x = (s + s[0..-2].reverse).to_i(10)
      break if x > LIMIT
      $all << x
    end
    from.upto(to - 1) do |i|
      s = i.to_s
      x = (s + s.reverse).to_i(10)
      break if x > LIMIT
      $all << x
    end
    from = to
    to *= 10
  end

  $ans = 2.upto(9).map do |d|
    $all.select do |i|
      s = i.to_s(d)
      s == s.reverse
    end
  end.to_a
end

